let currentLanguage = 'en';

function setLanguage(lang) {
  currentLanguage = lang;
  updateContent();
  localStorage.setItem('preferredLanguage', lang);
  
  // Update active language button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.remove('active');
  });
  document.querySelector(`[data-lang="${lang}"]`).classList.add('active');
}

function updateContent() {
  const t = translations[currentLanguage];
  
  // Update navigation
  document.querySelector('a[href="#about"]').textContent = t.about;
  document.querySelector('a[href="#skills"]').textContent = t.skills;
  document.querySelector('a[href="#port"]').textContent = t.projects;
  document.querySelector('a[href="#contact"]').textContent = t.contact;
  
  // Update mobile navigation
  document.querySelectorAll('#menu-content a[href="#about"]').forEach(el => el.textContent = t.about);
  document.querySelectorAll('#menu-content a[href="#skills"]').forEach(el => el.textContent = t.skills);
  document.querySelectorAll('#menu-content a[href="#port"]').forEach(el => el.textContent = t.projects);
  document.querySelectorAll('#menu-content a[href="#contact"]').forEach(el => el.textContent = t.contact);
  
  // Update hero section
  document.getElementById('type-name').textContent = t.role;
  document.querySelector('#apresentation p').innerHTML = t.welcome + '<br>' + t.description;
  document.querySelector('#apresentation a').textContent = t.downloadCV;
  
  // Update about section
  document.querySelector('#about h2').textContent = t.aboutTitle;
  document.querySelector('#right-side h2').textContent = t.getToKnow;
  const aboutParagraphs = document.querySelectorAll('#right-side p');
  aboutParagraphs[0].innerHTML = t.aboutText1.replace('Inteligência de mercado e análise de dados', `<span class="bg-primary">${t.aboutText1.includes('Market Intelligence') ? 'Market Intelligence and Data Analysis' : 'Inteligência de mercado e análise de dados'}</span>`);
  aboutParagraphs[1].innerHTML = t.aboutText2.replace('Desenvolvimento Web', `<span class="bg-primary">${t.webDev}</span>`).replace('Desenvolvimento Mobile', `<span class="bg-primary">${t.mobileDev}</span>`);
  
  // Update skills section
  document.querySelector('#skills h2').textContent = t.skillsTitle + ' :';
  
  // Update portfolio section
  document.querySelector('#port h2').textContent = t.portfolioTitle;
  
  // Update contact section
  document.querySelector('#contact h2').textContent = t.contactTitle;
  document.querySelector('#contact p').textContent = t.contactDescription;
  
  // Update footer
  document.querySelector('footer span').textContent = t.footerText;
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
  const savedLanguage = localStorage.getItem('preferredLanguage') || 'en';
  setLanguage(savedLanguage);
});